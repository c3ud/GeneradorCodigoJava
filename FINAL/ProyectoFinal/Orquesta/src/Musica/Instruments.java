package Musica;
public class Instruments {

    private String clarinete;



    private String trompeta;



    private String oboe;



    private String trombon;



    private String bombo;




    public String getClarinete(){
      return this.clarinete;
    }
    public String getTrompeta(){
      return this.trompeta;
    }
    public String getOboe(){
      return this.oboe;
    }
    public String getTrombon(){
      return this.trombon;
    }
    public String getBombo(){
      return this.bombo;
    }
    public void setClarinete(String value){
      this.clarinete = value;
    }
    public void setTrompeta(String value){
      this.trompeta = value;
    }
    public void setOboe(String value){
      this.oboe = value;
    }
    public void setTrombon(String value){
      this.trombon = value;
    }
    public void setBombo(String value){
      this.bombo = value;
    }

}